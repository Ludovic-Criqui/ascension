    public String[] affichagePodium() throws InterruptedException{
        String[] listeDesJoueurs = new String[10];
        PreparedStatement requete;
        int i =0;
        try {
            requete = c.prepareStatement("SELECT pseudo,podium FROM joueur ORDER BY podium DESC");
            ResultSet resultat = requete.executeQuery();
            while(resultat.next()){
                String pseudoJ = resultat.getString("pseudo");
                int podium = resultat.getInt("podium");
                if (podium!=0){
                listeDesJoueurs[i] = "Position nÂ°"+ String.valueOf(i+1) + " : " + pseudoJ;
                i+=1;
                jList1.setModel(new javax.swing.AbstractListModel<String>() {

                public int getSize() { return listeDesJoueurs.length; }
                public String getElementAt(int i) { return listeDesJoueurs[i]; }
                });
                }
            }
            requete.close();
        } catch (SQLException ex) {
            Logger.getLogger(FinDePartie.class.getName()).log(Level.SEVERE, null, ex);
        }
        Thread.sleep(1000);
        try {
            requete = c.prepareStatement("DELETE FROM joueur");
            requete.executeUpdate();
            requete.close();
        } catch (SQLException ex) {
            Logger.getLogger(SalonAttente.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listeDesJoueurs;
    }
    
